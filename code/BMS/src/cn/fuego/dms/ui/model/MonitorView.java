package cn.fuego.dms.ui.model;

import org.eclipse.swt.widgets.Label;

public class MonitorView
{
	private Label monitorName;
	private Label monitorValue;
	private Label monitorUnit;
	/**
	 * @return the monitorName
	 */
	public Label getMonitorName()
	{
		return monitorName;
	}
	/**
	 * @param monitorName the monitorName to set
	 */
	public void setMonitorName(Label monitorName)
	{
		this.monitorName = monitorName;
	}
	/**
	 * @return the monitorValue
	 */
	public Label getMonitorValue()
	{
		return monitorValue;
	}
	/**
	 * @param monitorValue the monitorValue to set
	 */
	public void setMonitorValue(Label monitorValue)
	{
		this.monitorValue = monitorValue;
	}
	/**
	 * @return the monitorUnit
	 */
	public Label getMonitorUnit()
	{
		return monitorUnit;
	}
	/**
	 * @param monitorUnit the monitorUnit to set
	 */
	public void setMonitorUnit(Label monitorUnit)
	{
		this.monitorUnit = monitorUnit;
	}

	
}
